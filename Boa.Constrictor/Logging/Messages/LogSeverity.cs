namespace Boa.Constrictor.Logging
{
    /// <summary>
    /// Enum for log severity levels.
    /// The higher the value, the more severe the level.
    /// </summary>
    public enum LogSeverity
    {
        Trace = 0,
        Debug = 1,
        Info = 2,
        Warning = 3,
        Error = 4,
        Fatal = 5,
    }
}
